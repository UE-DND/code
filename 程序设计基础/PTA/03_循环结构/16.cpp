/**********************************

请输出上半张九九乘法表，即上三角的半张。

1*1= 1 1*2= 2 1*3= 3 1*4= 4 1*5= 5 1*6= 6 1*7= 7 1*8= 8 1*9= 9 
       2*2= 4 2*3= 6 2*4= 8 2*5=10 2*6=12 2*7=14 2*8=16 2*9=18 
              3*3= 9 3*4=12 3*5=15 3*6=18 3*7=21 3*8=24 3*9=27 
                     ......
                                                 8*8=64 8*9=72 
                                                        9*9=81 

***********************************/

#include <stdio.h>

int main() {
    int i, j;
    for (i = 1; i <= 9; i++) {
        for (j = 1; j < i; j++) {
            printf("       ");
        }
        for (j = i; j <= 9; j++) {
            printf("%d*%d=%d ", i, j, i * j);
        }
        printf("\n");
    }

return 0;
}